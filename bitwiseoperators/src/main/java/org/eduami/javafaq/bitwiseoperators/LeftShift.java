package org.eduami.javafaq.bitwiseoperators;

public class LeftShift {
	public static void main(String[] args) {
		int i=1;
		System.out.println(i<<1);//left shift by one position
		System.out.println(i<<3);//left shift by three positions
		
		System.out.println(2<<1);//left shit 2 (i.e) 10 by one position (i.e) it becomes 100 which becomes numeric 4
		
	}
}
